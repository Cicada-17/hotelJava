package model;

public class Dwage {
	public String staffid;
	public String shijia;
	public String bingjia;
	public String bufa;
	public String chidao;
	public String getStaffid() {
		return staffid;
	}
	public void setStaffid(String staffid) {
		this.staffid = staffid;
	}
	public String getShijia() {
		return shijia;
	}
	public void setShijia(String shijia) {
		this.shijia = shijia;
	}
	public String getBingjia() {
		return bingjia;
	}
	public void setBingjia(String bingjia) {
		this.bingjia = bingjia;
	}
	public String getBufa() {
		return bufa;
	}
	public void setBufa(String bufa) {
		this.bufa = bufa;
	}
	public String getChidao() {
		return chidao;
	}
	public void setChidao(String chidao) {
		this.chidao = chidao;
	}
}
